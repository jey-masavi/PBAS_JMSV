def km_a_millas(km):
    """Convierte kil√≥metros a millas"""
    return km * 0.621371

def celsius_a_fahrenheit(celsius):
    """Convierte grados Celsius a Fahrenheit"""
    return (celsius * 9/5) + 32

def litros_a_galones(litros):
    """Convierte litros a galones"""
    return litros * 0.264172